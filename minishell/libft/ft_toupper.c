#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
	{
		c -= 32;
		return (c);
	}
	return (c);
}
/*The toupper() function returns the corresponding upper-case letter 
	if there is one; otherwise, the argument is returned unchanged.*/