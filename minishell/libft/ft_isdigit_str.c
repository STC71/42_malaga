#include "libft.h"

int	ft_isdigit_str(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (0);
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}

/*	This function checks that the input character string is all digits.
	Returns 1 if all characters are digits, 0 otherwise. */