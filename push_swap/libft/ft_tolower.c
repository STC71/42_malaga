#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (c + ('a' - 'A'));
	}
	return (c);
}

/*The tolower() function returns the corresponding lower-case letter if 
	there is one; otherwise, the argument is returned unchanged.*/